    public void listarProdutos(){
        model.setNumRows(0); // limpar tabela
        ResultSet rs = mysqlr.executeQuery("SELECT * FROM produto ORDER BY nome ASC");
        if (rs != null) {
            try {
                while (rs.next()) {                    
                    model.addRow(new String[]{rs.getString("id"),rs.getString("nome")});
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erro ao listar: " + e);
            }
        }
    }
