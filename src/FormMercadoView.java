    public void listarMercados(){
        model.setNumRows(0); // limpar tabela
        ResultSet rs = mysqlr.executeQuery("SELECT * FROM mercado ORDER BY nome ASC");
        if (rs != null) {
            try {
                while (rs.next()) {                    
                    model.addRow(new String[]{rs.getString("id"),rs.getString("nome"),rs.getString("email"),rs.getString("telefone"),rs.getString("endereco")});
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erro ao listar: " + e);
            }
        }
    }
